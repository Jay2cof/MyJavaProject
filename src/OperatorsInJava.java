public class OperatorsInJava {
    public static void main(String[] args) {

        // Arithmetic opt
        //Assignment opt
        //Conditional/ Relational / Congarision
        //Logical opt



        /*
        Arithmetic opt in java
        + addition
        - subtraction
        / division
        * Multiplication
        % Modulus
         */

        /*
        Assignment opt
        = Assign a value e.g. int number = 10;
        += Add and assign a value
        -= Subtract and assign a value
        /= Divide and assign a value
        *= Multiply and assign a value
        %= get modulus and assign the value
         */

        int num = 10;
        num *= 20;
        num -= 10;
        num /= 10;

        System.out.println(num);

        /*
        Comparative, Conditional opt
        == equals/compare two value
        > greater than
        < less than
        >= greater than or equals
        <= less than or equals
        != not equals
         */

        int x = 10;
        int y = 5;

        System.out.println(x + y );
        System.out.println(x <= y );
        System.out.println(x >= y );
        System.out.println(x != y );
        System.out.println( x >= y * 5 );


    }
}
